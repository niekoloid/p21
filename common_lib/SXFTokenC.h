#if !defined(SXFTokenC_INCLUDED_)
#define SXFTokenC_INCLUDED_

#include <afx.h>

//////////////////////////////////////////////////////////////////////
// 構文解析
//////////////////////////////////////////////////////////////////////
class SXFTokenC {
public:
//-----------------------------------------------------------------------------
//	デフォルトコンストラクタ
//-----------------------------------------------------------------------------
	SXFTokenC();

//-----------------------------------------------------------------------------
//	デフォルトデストラクタ
//-----------------------------------------------------------------------------
	~SXFTokenC();

//-----------------------------------------------------------------------------
//	型、改行、EOFを識別するための列挙型変数宣言
//-----------------------------------------------------------------------------
	enum TYPE {
		T_STRING = 0,
		T_INTEGER,
		T_REAL,
		T_PUNCT,
		T_ID,
		T_EOL,
		T_EOF,
	};
//-----------------------------------------------------------------------------
//	型、改行、EOFを識別するためのメンバ変数
//-----------------------------------------------------------------------------
	TYPE	m_Type;

//-----------------------------------------------------------------------------
//	文字列をセットする
//-----------------------------------------------------------------------------
	void Set(LPCSTR IN_Lps);

//-----------------------------------------------------------------------------
//	文字列からトークンを取得する
//-----------------------------------------------------------------------------
	BOOL GetToken();

//-----------------------------------------------------------------------------
//	文字列比較
//-----------------------------------------------------------------------------
	BOOL Is(LPCTSTR IN_str) {
		return m_string.Compare(IN_str) == 0;
	}
//-----------------------------------------------------------------------------
//	文字列
//-----------------------------------------------------------------------------
	CString		m_string;

//-----------------------------------------------------------------------------
//	EOFファイルを識別するためのフラグ
//-----------------------------------------------------------------------------
	BOOL		m_eof;

//-----------------------------------------------------------------------------
//	
//-----------------------------------------------------------------------------
	CString		m_source;

//-----------------------------------------------------------------------------
//
//-----------------------------------------------------------------------------
	LPCSTR		m_lps;

//-----------------------------------------------------------------------------
//
//-----------------------------------------------------------------------------
	int			m_cur;

//-----------------------------------------------------------------------------
//	
//-----------------------------------------------------------------------------
	int getc() { m_cur = *m_lps; if (m_cur) m_lps++; return m_cur; }

//-----------------------------------------------------------------------------
//	現在未使用
//-----------------------------------------------------------------------------
	CString		CurText();
};

#endif // !defined(SXFTokenC_INCLUDED_)
